    def test_build_dashboard_payload_aggregates_cells_and_tags(self):
        payload = build_dashboard_payload(self.rows)

        self.assertEqual(payload["meta"]["default_period"], "近一周")
        self.assertEqual(payload["meta"]["target_companies"][0], "易方达")
        self.assertEqual(payload["meta"]["periods"], PERIODS)

        matrix_index = {
            (cell["company"], cell["primary_category"]): cell
            for cell in payload["matrix"]
        }

        efund_cell = matrix_index[("易方达", "核心宽基")]
        self.assertTrue(efund_cell["is_covered"])
        self.assertEqual(efund_cell["product_count"], 2)
        self.assertAlmostEqual(efund_cell["total_scale"], 160.0)
        self.assertIn("领先布局", efund_cell["base_tags"])
        self.assertIn("近一周", efund_cell["period_metrics"])
        self.assertIn("强势流入", efund_cell["period_metrics"]["近一周"]["tags"])

        yongying_cell = matrix_index[("永赢", "核心宽基")]
        self.assertFalse(yongying_cell["is_covered"])
        self.assertIn("空白赛道", yongying_cell["base_tags"])

        commodity_context = payload["market_context"]["商品与另类"]
        self.assertEqual(commodity_context["market_product_count"], 4)
        self.assertAlmostEqual(commodity_context["market_total_scale"], 360.0)

        product = next(item for item in payload["products"] if item["fund_code"] == "510310.OF")
        self.assertEqual(product["structure_tags"], ["核心宽基", "沪深300"])
        self.assertIn("领先布局", product["competition_tags"])
        self.assertIn("近一周", product["strong_inflow_periods"])

    def test_render_dashboard_html_embeds_bootstrap_payload(self):
        payload = build_dashboard_payload(self.rows)
        html = render_dashboard_html(payload)

        self.assertIn("ETF竞争对手产品矩阵", html)
        self.assertIn("__ETF_MATRIX_DATA__", html)
        self.assertIn("近一周", html)
        self.assertIn("强势流入", html)


