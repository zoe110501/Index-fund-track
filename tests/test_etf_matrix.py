class CategoryMappingTests(unittest.TestCase):
    def test_map_primary_category_for_representative_buckets(self):
        expected = {
            "沪深300": "核心宽基",
            "中证A500": "核心宽基",
            "港股科技": "科技TMT",
            "黄金": "商品与另类",
            "普通红利": "红利与SmartBeta",
            "国防军工": "高端制造与周期",
            "非A生科,医药医疗": "医药健康",
            "沙特": "跨境与区域市场",
            "央企科技": "央国企与政策主题",
        }

        for raw_category, primary_category in expected.items():
            with self.subTest(raw_category=raw_category):
                self.assertEqual(map_primary_category(raw_category), primary_category)

    def test_mapping_table_covers_known_categories_without_empty_values(self):
        self.assertGreaterEqual(len(RAW_TO_PRIMARY_CATEGORY), 88)
        self.assertNotIn("", RAW_TO_PRIMARY_CATEGORY)
        self.assertTrue(all(value for value in RAW_TO_PRIMARY_CATEGORY.values()))


